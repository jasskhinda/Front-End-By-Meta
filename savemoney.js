var needmoney = 0; // Start with 0 dollars saved

while (needmoney < 10) { // Keep running while the savings are less than 10 dollars
    console.log("I have saved " + needmoney + " today");
    needmoney++; // Save 1 dollar, increase the counter
}

console.log("I have saved enough money to buy food today"); // Loop stops at 10 dollars
